import { readFile, readdir, rename, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { parse } from "smol-toml";
import { validateThemePackage } from "../packages/theme-tooling/src/theme-manifest.mjs";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const rootPackage = JSON.parse(await readFile(join(root, "package.json"), "utf8"));
const configPath = resolve(process.env.ORG_ZHIXING_CONFIG ?? join(root, "public/org-zhixing.toml"));
const outputPath = resolve(
  process.env.ORG_ZHIXING_THEME_REGISTRY_OUTPUT ?? join(root, "src/generated/themeRegistry.ts"),
);
const siteConfig = parse(await readFile(configPath, "utf8"));
const packageDirectories = await discoverWorkspacePackages(rootPackage.workspaces ?? []);
const themes = [];

for (const directory of packageDirectories) {
  const packageJsonPath = join(directory, "package.json");
  const packageJson = await readPackageJson(packageJsonPath);
  if (!packageJson) continue;
  if (!packageJson.orgZhixing) continue;
  themes.push(validateThemePackage(packageJson, relative(root, packageJsonPath)));
}

async function readPackageJson(path) {
  try {
    return JSON.parse(await readFile(path, "utf8"));
  } catch (error) {
    if (error?.code === "ENOENT") return null;
    throw error;
  }
}

themes.sort((left, right) => left.id.localeCompare(right.id));
const duplicate = themes.find((theme, index) => theme.id === themes[index - 1]?.id);
if (duplicate) {
  const packages = themes
    .filter((theme) => theme.id === duplicate.id)
    .map((theme) => theme.package);
  throw new Error(`THEME-E002 duplicate theme id "${duplicate.id}": ${packages.join(", ")}`);
}

const selectedId = typeof siteConfig.theme === "string" ? siteConfig.theme : "elegant-blog";
const selectedVariant =
  typeof siteConfig.theme_variant === "string" ? siteConfig.theme_variant : "default";
const selected = themes.find((theme) => theme.id === selectedId);
if (!selected) {
  throw new Error(
    `THEME-E001 unknown theme "${selectedId}"; available: ${themes.map((theme) => theme.id).join(", ")}`,
  );
}
if (!selected.variants.includes(selectedVariant)) {
  throw new Error(
    `THEME-E005 unknown variant "${selectedVariant}" for theme "${selectedId}"; available: ${selected.variants.join(", ")}`,
  );
}

const catalog = themes.map(({ id, package: packageName, defaultVariant, variants }) => ({
  id,
  package: packageName,
  defaultVariant,
  variants,
}));
const imports = themes
  .map((theme, index) => `import theme${index} from ${JSON.stringify(theme.package)};`)
  .join("\n");
const themeBindings = themes.map((_theme, index) => `theme${index}`).join(", ");
const entries = themes
  .map((theme, index) => `  [${JSON.stringify(theme.id)}, theme${index}]`)
  .join(",\n");
const output = `${imports}\n\n// Generated by scripts/generate-theme-registry.mjs.\nexport const generatedThemeCatalog = ${JSON.stringify(catalog, null, 2)} as const;\n\nexport const generatedThemes = [${themeBindings}] as const;\n\nexport const generatedThemeEntries = [\n${entries},\n] as const;\n`;
const temporaryOutputPath = `${outputPath}.${process.pid}.tmp`;
await writeFile(temporaryOutputPath, output);
await rename(temporaryOutputPath, outputPath);
console.log(
  `theme-registry selected=${selected.id} variant=${selectedVariant} catalog=${themes.length}`,
);

async function discoverWorkspacePackages(patterns) {
  const directories = [];
  for (const pattern of patterns) {
    if (!pattern.endsWith("/*")) {
      throw new Error(`unsupported workspace pattern: ${pattern}`);
    }
    const parent = join(root, pattern.slice(0, -2));
    for (const entry of await readdir(parent, { withFileTypes: true })) {
      if (entry.isDirectory()) directories.push(join(parent, entry.name));
    }
  }
  return directories;
}
